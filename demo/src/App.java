public class App {
  public static void main(String[] args) {
    System.out.println("Hello, World!");
    System.out.println("I love lasagna!");
    System.out.print("I love pizza!\n");
    System.out.println("Peace, love and other shit.\n");
    System.out.println("\tI feel good.\n");
    System.out.println("\"Quotes\".");
    // This is a comment
    /*
     * This is a long comment.
     * It's multiline. 
     */
    /*
     * Variables
     */

     int x = 123;
     System.out.println("x number is " + x);

     int y; // declaration
     y = 456; // assignment

     int z = 789; // initialization

     System.out.println("y number is " + y);
     System.out.println("z number is " + z);
     
     long l = 123456789123456789L;

     System.out.println("l number is " + l);

     double d = 3.14;
     boolean b = true;

     System.out.println("d number is " + d);
     System.out.println("b number is " + b);

     char symbol = '@';
     String name = "Name";
     
     System.out.println("symbol number is " + symbol);
     System.out.println("name variable is " + name);
  }
}
